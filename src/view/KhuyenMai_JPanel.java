    private String getTrangThai(String ngayBD, String ngayKT) {
        Date now = XDate.now();
        if (now.after(XDate.toDate(ngayBD, ""))
                && now.before(XDate.toDate(ngayKT, ""))) {
            return "Đang diễn ra";
        } else if (now.after(XDate.toDate(ngayKT, ""))) {
            return "Đã kết thúc";
        } else {
            return "Chưa diễn ra";
        }
    }
    
