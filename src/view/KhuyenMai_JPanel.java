    public KhuyenMai getKM() {
        KhuyenMai km = new KhuyenMai();
        km.setMaKhuyenMai(txt_MaKhuyenMai.getText());
        km.setTenChuongTrinh(txt_TenChuongTrinh.getText());
        km.setNgayBatDau(XDate.toString(txt_NgayBatDau.getDate(), ""));
        km.setNgayKetThuc(XDate.toString(txt_NgayKetThuc.getDate(), ""));
        km.setMoTa(txt_MoTa.getText());
        km.setDonViGiam((String) cbo_DonViGiam.getSelectedItem());
        if (rdo_HoatDong.isSelected()) {
            km.setTrangThai(1);
        } else {
            km.setTrangThai(0);
        }

        km.setGiatriGiam(Integer.valueOf(txt_GiatriGiam.getText()));
        return km;
    }

    public boolean checkForm() {
        String maKM = txt_MaKhuyenMai.getText().trim();
        if (maKM.isEmpty()) {
            DialogHelper.alert(this, "Ma khuyen mai khonh duoc de trong");
            return false;
        }
        String tenKM = txt_TenChuongTrinh.getText().trim();
        if (tenKM.isEmpty()) {
            DialogHelper.alert(this, "Ten khuyen mai khonh duoc de trong");
            return false;
        }
        try {
            int giaTriGiam = Integer.parseInt(txt_GiatriGiam.getText().trim());
            if (giaTriGiam < 0) {
                DialogHelper.alert(this, "Gia tri giam phai lon hon 0");
                return false;
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Gia tri giam phai la so");
            return false;
        }

        String moTa = txt_MoTa.getText().trim();
        if (moTa.isEmpty()) {
            DialogHelper.alert(this, "Mo ta khonh duoc de trong");
            return false;
        }
        if(txt_NgayBatDau.getDate().after(txt_NgayKetThuc.getDate())){
            DialogHelper.alert(null, "Ngày bắt đầu phải sau ngày kết thúc");
            return false;
        }
        return true;
    }

